// O operador new cria e inicializa um novo objeto. A palavra-chave new deve ser seguida de uma chamada de função. Uma função usada dessa maneira é chamada de construtora e serve para inicializar um objeto recém-criado. JavaScript básica contém construtoras internas para tipos nativos.

var o = new Object(); // Cria um objeto vazio: o mesmo que {}
var a = new Array(); // Cria um array vazio: o mesmo que []
var d = new Date(); // Cria um objeto Date representado a hora atual
var r = new RegExp(); // Cria um objeto RegExp para comparação de padrões;
